dx = [-1, 0, 1, 0]
dy = [0, 1, 0, -1]

def cut(m):
    global N, M
    new = [[0] * M for _ in range(N)]
    for i in range(1, N - 1):
        for j in range(1, M - 1):
            if m[i][j] != 0:
                cnt = 0
                for k in range(4):
                    if m[i + dx[k]][j + dy[k]] == 0:
                        cnt += 1
                new[i][j] = m[i][j] - cnt
                if new[i][j] < 0:
                    new[i][j] = 0
    return new

N, M = map(int, input().split())
m = [0] * N
for i in range(N):
    m[i] = list(map(int, input().split()))

result, ans = 0, 0
queue = []
visited = [[0] * M for _ in range(N)]
for i in range(1, N - 1):
    for j in range(1, M - 1):
        if m[i][j] and not visited[i][j]:
            queue.append((i, j))
            visited[i][j] = 1
            while len(queue):
                x, y = queue.pop(0)
                for k in range(4):
                    if m[x + dx[k]][y + dy[k]] and not visited[x + dx[k]][y + dy[k]]:
                        queue.append((x + dx[k], y + dy[k]))
                        visited[x + dx[k]][y + dy[k]] = 1
            result += 1

while result < 2:
    result = 0
    m = cut(m)
    visited = [[0] * M for _ in range(N)]
    for i in range(1, N - 1):
        for j in range(1, M - 1):
            if m[i][j] and not visited[i][j]:
                queue.append((i, j))
                visited[i][j] = 1
                while len(queue):
                    x, y = queue.pop(0)
                    for k in range(4):
                        if m[x + dx[k]][y + dy[k]] and not visited[x + dx[k]][y + dy[k]]:
                            queue.append((x + dx[k], y + dy[k]))
                            visited[x + dx[k]][y + dy[k]] = 1
                result += 1
    if result == 0:
        ans = 0
        break
    ans += 1

print(ans)

